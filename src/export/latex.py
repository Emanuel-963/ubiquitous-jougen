"""LaTeX booktabs table exporter for scientific manuscripts.

Generates ``.tex`` files containing publication-quality tables that can be
``\\input{}``-ed directly into an academic paper.  Two table types are
supported:

1. **Circuit fitting parameters** — one row per sample, columns = circuit
   element values with ± uncertainties.
2. **EIS feature ranking** — one row per sample, columns = Score, Rank,
   Rs, Rp, C_dl, σ_W (Warburg), and other derived features.

Both tables use the ``booktabs`` package (``\\toprule`` / ``\\midrule`` /
``\\bottomrule``) and ``siunitx`` for number formatting where available.

The exporter also accepts plain ``raw_eis`` DataFrames and writes a simple
frequency/impedance table in that case.

Output format
-------------
::

    \\begin{table}[htbp]
      \\centering
      \\caption{...}
      \\label{tab:...}
      \\begin{tabular}{lSSS}
        \\toprule
        Sample & {Z'} & {-Z''} & {|Z|} \\\\
        \\midrule
        Sample A & 12.3 & 3.1 & 12.7 \\\\
        ...
        \\bottomrule
      \\end{tabular}
    \\end{table}
"""

from __future__ import annotations

import logging
import re
from pathlib import Path
from typing import List, Optional

import pandas as pd

from .base import EISExporter, ExportError

logger = logging.getLogger(__name__)

__all__ = ["LaTeXExporter", "export_circuit_table_latex", "export_ranking_latex"]

# Characters that need escaping in LaTeX
_LATEX_SPECIAL = re.compile(r"([&%$#_{}~^\\])")


def _tex_escape(text: str) -> str:
    """Escape LaTeX special characters in plain text."""

    def _replace(m: re.Match) -> str:
        ch = m.group(1)
        mapping = {
            "&": r"\&",
            "%": r"\%",
            "$": r"\$",
            "#": r"\#",
            "_": r"\_",
            "{": r"\{",
            "}": r"\}",
            "~": r"\textasciitilde{}",
            "^": r"\textasciicircum{}",
            "\\": r"\textbackslash{}",
        }
        return mapping.get(ch, ch)

    return _LATEX_SPECIAL.sub(_replace, str(text))


def _fmt_val(val: object, decimals: int = 4) -> str:
    """Format a numeric value for LaTeX siunitx, or return escaped string."""
    try:
        f = float(val)  # type: ignore[arg-type]
        if abs(f) >= 1e4 or (abs(f) < 1e-3 and f != 0.0):
            return f"{f:.{decimals}e}"
        return f"{f:.{decimals}f}"
    except (TypeError, ValueError):
        return _tex_escape(str(val))


class LaTeXExporter(EISExporter):
    """Export EIS raw spectrum data as a LaTeX booktabs frequency table.

    For circuit fitting parameters / ranking tables, use the standalone
    functions :func:`export_circuit_table_latex` and
    :func:`export_ranking_latex`.
    """

    DEFAULT_EXTENSION = ".tex"
    FORMAT_NAME = "LaTeX booktabs (.tex)"

    def export_dataframe(
        self,
        df: pd.DataFrame,
        out_path: Path,
        *,
        sample_name: str = "",
        extra_meta: dict | None = None,
    ) -> Path:
        """Write a frequency/impedance table for a single EIS spectrum.

        Columns written: Frequency (Hz), Z' (Ω), -Z'' (Ω), |Z| (Ω).
        """
        data = self._validate_df(df).copy()
        data.dropna(subset=["frequency", "zreal", "zimag"], inplace=True)
        if data.empty:
            raise ExportError(f"No valid data rows for '{sample_name}'.")

        data["|Z|"] = (data["zreal"] ** 2 + data["zimag"] ** 2) ** 0.5
        neg_zimag = -data["zimag"]

        caption = _tex_escape(sample_name or "EIS Spectrum")
        label = "tab:" + re.sub(r"[^a-zA-Z0-9]", "_", sample_name or "eis")

        rows: List[str] = []
        for _, row in data.iterrows():
            rows.append(
                f"    {_fmt_val(row['frequency'], 4)} & "
                f"{_fmt_val(row['zreal'], 4)} & "
                f"{_fmt_val(neg_zimag[row.name], 4)} & "
                f"{_fmt_val(row['|Z|'], 4)} \\\\"
            )

        tex = _build_table(
            caption=caption,
            label=label,
            col_spec="SSSS",
            headers=[
                r"$f$ / \si{\hertz}",
                r"$Z'$ / \si{\ohm}",
                r"$-Z''$ / \si{\ohm}",
                r"$|Z|$ / \si{\ohm}",
            ],
            rows=rows,
            note=f"Generated by IonFlow Pipeline — {self._now_str()}",
        )

        out_path = Path(out_path)
        out_path.parent.mkdir(parents=True, exist_ok=True)
        out_path.write_text(tex, encoding="utf-8")
        return out_path


